

def add(num1, num2):
    result = num1 + num2
    print(f"Result: {result}")


def subtract(num1, num2):
    result = num1 - num2
    print(f"Result: {result}")

def multiply(num1, num2):
    result = num1 * num2
    print(f"Result: {result}")


def divide(num1, num2):
    result = num1 / num2
    print(f"Result: {result}")